    private void ActualizarCat_DepartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ActualizarCat_DepartActionPerformed
        // TODO add your handling code here:
        try {
            String ID_DEPARTAMENTO = idDepart;
            String DESC_Depart = txtdescdepar.getText();

            PreparedStatement psd = cn.prepareStatement("UPDATE cat_departamento SET DESC_DEPARTAMENTO='" +
                DESC_Depart +

                "' WHERE ID_DEPARTAMENTO= '"
                +ID_DEPARTAMENTO + "'");

            int n = psd.executeUpdate();
            System.out.println(n);
            if (n > 0) {
                JOptionPane.showMessageDialog(null, "Registro Actualizado");
            } else {
                JOptionPane.showMessageDialog(null, "No Se Puede Actualizar Tu Registro ");
            }
            catalodepartamento("");
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }

    }//GEN-LAST:event_ActualizarCat_DepartActionPerformed

