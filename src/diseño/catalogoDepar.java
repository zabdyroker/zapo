 void catalodepartamento (String valor){
    DefaultTableModel modelo= new DefaultTableModel();
    modelo.addColumn("ID DEL DEPARTAMENTO");
    modelo.addColumn("DESCRIPCION DEL DEPARTAMENTO");
    
    
    tabladepartamento.setModel(modelo);
    String sql="";
    if(valor.equals(""))
    {
        sql="SELECT * FROM cat_departamento";
    }
    else{
        sql="SELECT * FROM cat_departamento WHERE ID_DEPARTAMENTO='"+valor+"'";
    }
 
    String []datos = new String [2];
        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while(rs.next()){
                datos[0]=rs.getString(1);
                
                datos[1]=rs.getString(2);
                
                modelo.addRow(datos);
            }
            tabladepartamento.setModel(modelo);
        } catch (SQLException ex) {
            Logger.getLogger(COnsul.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }
