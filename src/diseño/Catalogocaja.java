 void catalocaja (String valor){
    DefaultTableModel modelo= new DefaultTableModel();
    modelo.addColumn("ID DE LA CAJA");
    modelo.addColumn("DESCRIPCION DE LA CAJA");
    modelo.addColumn("DISPONIBILIDAD");
    modelo.addColumn("USUARIO");
    
    
    tablacaja.setModel(modelo);
    String sql="";
    if(valor.equals(""))
    {
        sql="SELECT * FROM cat_cajas";
    }
    else{
        sql="SELECT * FROM cat_cajas WHERE ID_CAJA='"+valor+"'";
    }
 
    String []datos = new String [4];
        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while(rs.next()){
                datos[0]=rs.getString(1);
                datos[1]=rs.getString(2);
                datos[2]=rs.getString(3);
                datos[3]=rs.getString(4);
               
                modelo.addRow(datos);
            }
            tablacaja.setModel(modelo);
        } catch (SQLException ex) {
            Logger.getLogger(COnsul.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }
