    void detalleventas (String valor){
    DefaultTableModel modelo= new DefaultTableModel();
    modelo.addColumn("ID DEL PRODUCTO");
    modelo.addColumn("FOLIO");
    modelo.addColumn("CANTIDAD");
    modelo.addColumn("PESO UNITARIO");
    modelo.addColumn("IVA");
    
    tabladetalle.setModel(modelo);
    String sql="";
    if(valor.equals(""))
    {
        sql="SELECT * FROM detalle_ventas";
    }
    else{
        sql="SELECT * FROM detalles_ventas WHERE ID_PRODUCTO='"+valor+"'";
    }
 
    String []datos = new String [5];
        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while(rs.next()){
                datos[0]=rs.getString(1);
                datos[1]=rs.getString(2);
                datos[2]=rs.getString(3);
                datos[3]=rs.getString(4);
                datos[4]=rs.getString(5);
                
                modelo.addRow(datos);
            }
            tabladetalle.setModel(modelo);
        } catch (SQLException ex) {
            Logger.getLogger(COnsul.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }
