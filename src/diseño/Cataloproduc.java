        sql= "INSERT INTO cat_productos (DESC_PRODUCO,ID_UNIDAD_MEDIDA,CANTIDAD,P_U_COMPRA,P_U_VENTA,ID_DEPARTAMENTO,CANTIDAD_MIN,IVA,P_U_MAYOREO,P_S_MAYOREO) VALUES (?,?,?,?,?,?,?,?,?,?)";
        try {
            PreparedStatement psd = cn.prepareStatement(sql);

            psd.setString(1,Desc_produc);
            int s = Integer.parseInt((String) comboID_Unidad_Medida.getSelectedItem());
            psd.setInt(2,s);
            psd.setString(3,Cantidad_medida);
            psd.setString(4,PesoUC);
            psd.setString(5,PesoUV);
            int l = Integer.parseInt((String) comboIDdescripcion.getSelectedItem());
            psd.setInt(6,l);
            psd.setString(7,CanPro);
            psd.setString(8,Iva);
            psd.setString(9,PUM);
            psd.setString(10,PSM);
            int n= psd.executeUpdate();

            if (n>0){

                JOptionPane.showMessageDialog(null, "REGISTRO GUARDADO CON EXITO");

            }
        } catch (SQLException ex) {
            Logger.getLogger(usuario.class.getName()).log(Level.SEVERE, null, ex);
        }
